    private void tabla() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            tblEmpleados.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            Connection conn = con.conectar();
            String sql = "SELECT codigo,nombre,apellidoPaterno,apellidoMaterno,rfc FROM ms_empleado WHERE ID_EMPRESA = (SELECT ID_EMPRESA FROM ms_empresa WHERE nombre = '" + login.nomEmpresa + "') AND estatus = 1";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            ResultSetMetaData rsMd = rs.getMetaData();
            int countRow = rsMd.getColumnCount();

            modelo.addColumn("CÃ“DIGO");
            modelo.addColumn("NOMBRE");
            modelo.addColumn("APELLIDO PATERNO");
            modelo.addColumn("APELLIDO MATERNO");
            modelo.addColumn("RFC");

            while (rs.next() == true) {
                Object[] row = new Object[countRow];
                for (int i = 0; i < countRow; i++) {
                    row[i] = rs.getObject(i + 1);
                }
                modelo.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.exit(0);
        }
        /*finally
        {
            con.desconectar();
        }*/
    }


